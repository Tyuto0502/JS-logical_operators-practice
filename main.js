//AND(&&)
var numA = 10;
var numB = 20;
if (numA === 10 && numB === 20) {
  console.log("true処理");
  console.log(numA === 10 && numB === 20);
} else {
  console.log("false処理");
  console.log(numA === 10 && numB === 20);
}


//OR(||)
var numA = 10;
var numB = 20;
if (numA === 10 || numB === 20) {
  console.log("true処理");
  console.log(numA === 10 || numB === 20);
} else {
  console.log("false処理");
  console.log(numA === 10 || numB === 20);
}


//NOT(!)
var num = 123;
if (num != 100) {
  console.log("true処理");
  console.log(num);
} else {
  console.log("false処理");
  console.log(num);
}